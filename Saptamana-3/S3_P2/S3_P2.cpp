#include <cstdio>

#include "Canvas.h"
int main()
{
    Canvas MyCanvas(50,50);
    printf("========================================================================= CANVAS ================================================================================================================ \n");
    MyCanvas.SetPoint(0,0,'O');
    MyCanvas.Print();
    MyCanvas.Clear();
    printf("========================================================================= CANVAS ================================================================================================================ \n");
    MyCanvas.DrawLine(0,0,10,5,'X');
    MyCanvas.Print();
    MyCanvas.Clear();
    printf("========================================================================= CANVAS ================================================================================================================ \n");
    MyCanvas.DrawRect(10,10,40,40,'X');
    MyCanvas.Print();
    MyCanvas.Clear();
    printf("========================================================================= CANVAS ================================================================================================================ \n");
    MyCanvas.FillRect(10,10,40,40,'X');
    MyCanvas.Print();
    MyCanvas.Clear();
    printf("========================================================================= CANVAS ================================================================================================================ \n");
    MyCanvas.DrawCircle(20,20,15,'X');
    MyCanvas.Print();
    MyCanvas.Clear();
    printf("========================================================================= CANVAS ================================================================================================================ \n");
    MyCanvas.FillCircle(20,20,15,'X');
    MyCanvas.Print();
    MyCanvas.Clear();
    
}
